# 手动控制进度
# -----------------------------------------------
# 手动更新进度（如下载分块文件）
with tqdm(total=100, desc="下载进度", unit="MB") as pbar:
    for chunk in range(10):
        time.sleep(1)  # 模拟下载
        pbar.update(10)  # 每次更新10个单位
