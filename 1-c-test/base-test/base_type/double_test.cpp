#include <iostream>
using namespace std;

int main() {
    float f1 = 3.14f;
    double f2 = 3.14;
    cout << f1 << endl;
    cout << f2 << endl;

    // 科学计数法
    float f3 = 3e2; // 300
    float f4 = 3e-2; // 0.03
    cout << f3 << endl;
    cout << f4 << endl;

    return 0;
}
